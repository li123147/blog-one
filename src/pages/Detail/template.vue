<h2 id="引言">引言</h2>
<p>B站在OGV(Occupationally Generated Video， 专业机构生产内容)板块，可供商业化的国产动画、综艺、纪录片作品则比以往更加细分。加速商业生态开放的背后，B站的UP主+OGV的内容生态布局已显露 。</p>
<p>B站从创建开始到现在，一直有着一些非常好的东西吸引着我。一个是“学习内容”方面；另一个是“创作激励”方面。在人们日益网络化的时代，几乎每个人都离不开互联网。B站恰好运用其独特之道，向人们展现出它别具一格的魅力。通过结合在B站上学习前端知识的几年中，我油然而生的要为B站写一篇报道。</p>
<p>本报道目的在于介绍B站一些有趣的UP主的东西，并不存在商业用途。因为有着对它独特的情愫，我会一直将这个推荐做下去，作为一个可以抵挡当代单身年轻人孤独的一座堤坝。</p>
<h2 id="冲浪普拉斯">冲浪普拉斯</h2>
<p>这是我在B站看的第一个较为完整阐述中国商业史的心路历程。从王公子的熊猫TV的崛起，跻身前三的直播行业，到卖身自救；从宇宙外卖大战的雏形，平分天下的美团和饿了么，到BAT三大巨头背后的明争暗斗；从当代毕业生毕业租房，到被迫签下房租贷，房租秒变高利贷背后的资本操作......正如UP主所言：在时代的的浪潮中，你细细观察总会看到不一样的资本博弈。</p>
